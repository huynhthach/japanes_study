     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $keyword = $request->input('keyword');
    
        // Lấy tất cả các comment từ database
        $comments = commemt::where('Content', 'like', "%$keyword%")->orderBy('created_at', 'desc')->paginate(10);
        return view('admin.management.comment_manager',compact('comments'));
    }

