    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        // Tìm comment cần xóa
        $comment = commemt::where('Comment_ID',$id);

        // Xóa comment
        $comment->delete();

        // Chuyển hướng về trang hiển thị tất cả các comment
        return redirect()->route('comment.index')->with('success', 'Comment đã được xóa thành công');
